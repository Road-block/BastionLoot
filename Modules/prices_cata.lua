-- The default quality threshold:
-- 0 - Poor
-- 1 - Uncommon
-- 2 - Common
-- 3 - Rare
-- 4 - Epic
-- 5 - Legendary
-- 6 - Artifact
local quality_threshold = 4

local function getStaticValues(itemID, itemQuality, itemLevel, itemEquipLoc)
  -- Check our item database
  if CACHED_ITEM_DATA[itemID] then
    itemQuality, itemLevel, itemEquipLoc = unpack(CACHED_ITEM_DATA[itemID])
  end

  -- Check to see if there is custom data for this item ID
  if CUSTOM_ITEM_DATA[itemID] then
    itemQuality, itemLevel, itemEquipLoc = unpack(CUSTOM_ITEM_DATA[itemID])
  end

  -- Is the item above our minimum threshold?
  if not itemQuality or itemQuality < quality_threshold then
    return nil, nil, itemLevel, itemQuality, itemEquipLoc
  end

  local slot_multiplier1 = EQUIPSLOT_MULTIPLIER_1[itemEquipLoc]
  local slot_multiplier2 = EQUIPSLOT_MULTIPLIER_2[itemEquipLoc]

  if not slot_multiplier1 then
    return nil, nil, itemLevel, itemQuality, itemEquipLoc
  end
  local gp_base = 4.83 * 2 ^ (itemLevel/75 + (itemQuality - 4)) -- ~20% increase per tier (13ilvl)
  local high = math.floor(gp_base * slot_multiplier1)
  local low = slot_multiplier2 and math.floor(gp_base * slot_multiplier2) or nil
  return high, low
end
