    tier = "T11.5"
  elseif ilevel <= 390 then
    tier = "T12"
  elseif ilevel <= 396 then
    tier = "T12.5"
  elseif ilevel <= 402 or ilevel == 403 then
    tier = "T13"
  elseif ilevel >= 410 then
    tier = "T13.5"
  end
  return tier
end

local function GetDiscountDetail(invType, classID, subclassID)
  --[[Bit of documentation here
  Some discounts are baked-in for historical reasons of how the epgp price calculation worked off item equip location. Equip location lumps together some items (eg wands + some ranged weapon types) that have conditional discounts (based on class and role which is dynamic) so a human was needed to apply this manually. The formula just returned "this equiplocation has 2 possible valuations, you pick the correct one".

  We can only decide discount status deterministically for certain combinations.
  Two-Handed melee weapon to Hunter
  Main-hand or One-hand melee weapon to Hunter (Off-hand melee weapon has discount baked-in)
  Shield to Shaman (as the only non-tank shield bearer)
  Any ranged weapon to melee (ie Warrior or Rogue)
  All Wands (epgp formula is done based on inventory type and they share IndexRangedRight with some ranged weapons)

  Other cases still need a human deciding (One-hand melee weapon to Tank for tanking, One-hand melee weapon to tank or melee for Dual Wield Offhand use, Shield to Paladin, Two-hand melee weapon to Titan Grip warrior for Offhand use)
  ]]
  local wand_discount = subclassID == Enum.ItemWeaponSubclass.Wand
  local ranged_discount = ((invType == Enum.InventoryType.IndexRangedType) or (invType == Enum.InventoryType.IndexRangedrightType)) and "WARRIOR:ROGUE"
  local shield_discount = (subclassID == Enum.ItemArmorSubclass.Shield) and "SHAMAN"
  local onehand_discount = ((invType == Enum.InventoryType.IndexWeaponType) or (invType == Enum.InventoryType.IndexWeaponmainhandType)) and "HUNTER"
  local twohand_discount = (invType == Enum.InventoryType.Index2HweaponType) and "HUNTER"
  return wand_discount,ranged_discount,shield_discount,onehand_discount,twohand_discount
end

function bepgp_prices_cata:GetPrice(item,progress)
  if not (type(item)=="number" or type(item)=="string") then return end
  if not progress then progress = "T13" end
  local price1,price2,itemID,data,tier,useful
  local itemType, itemSubType, itemEquipLoc, icon, classID, subclassID
  local wand_discount,ranged_discount,shield_discount,onehand_discount,twohand_discount, item_level
  itemID, itemType, itemSubType, itemEquipLoc, icon, classID, subclassID = bepgp.GetItemInfoInstant(item)
  if (itemID) then
    local invType = bepgp.GetItemInventoryTypeByID(itemID)
    data = prices[itemID]
    if (data) then
      local quality, ilevel, equip = unpack(data)
      item_level = tonumber(ilevel)
      tier = calcTier(ilevel)
      price1, price2 = getStaticValues(itemID, quality, ilevel, equip)
      wand_discount,ranged_discount,shield_discount,onehand_discount,twohand_discount = GetDiscountDetail(invType, classID, subclassID)
    else
      return
    end
  end
  return price1, tier, price2, wand_discount,ranged_discount,shield_discount,onehand_discount,twohand_discount, item_level
end

function bepgp_prices_cata:OnEnable()
  local system = {func=bepgp_prices_cata.GetPrice,flavor="_cata"}
  bepgp:RegisterPriceSystem(name_version,system)
  local mzt,_,_,_,reason = bepgp.GetAddOnInfo("MizusRaidTracker")
  if not (reason == "ADDON_MISSING" or reason == "ADDON_DISABLED") then
    local loading, finished = bepgp.IsAddOnLoaded("MizusRaidTracker")
    if loading and finished then
      self:ADDON_LOADED("ADDON_LOADED","MizusRaidTracker")
    else
      self:RegisterEvent("ADDON_LOADED")
    end
  end
end

function bepgp_prices_cata:ADDON_LOADED(event,...)
  if ... == "MizusRaidTracker" then
    self:UnregisterEvent("ADDON_LOADED")
    local MRT_ItemCost = function(mrt_data)
      local itemstring = mrt_data.ItemString
      local dkpValue = self:GetPrice(itemstring, bepgp.db.profile.progress)
      local itemNote
      if not dkpValue then
        dkpValue = 0
        itemNote = ""
      else
        local dkpValue2 = math.floor(dkpValue*bepgp.db.profile.discount)
        itemNote = string.format("%d or %d", dkpValue, dkpValue2)
      end
      return dkpValue, mrt_data.Looter, itemNote, "", true
    end
    if MRT_RegisterItemCostHandlerCore then
      MRT_RegisterItemCostHandlerCore(MRT_ItemCost, addonName)
    end
  end
end

local cacheTimer
function bepgp_prices_cata:Recalculate()
  BastionLootDB._CACHE_ITEM_DATA = BastionLootDB._CACHE_ITEM_DATA or {}
  local stats = {}
  local count = 0
  for i=50000,80000 do
    local itemID, itemType, itemSubType, itemEquipLoc, icon, classID, subclassID = bepgp.GetItemInfoInstant(i)
    if itemID and EQUIPSLOT_MULTIPLIER_1[itemEquipLoc] then
      if not BastionLootDB._CACHE_ITEM_DATA[itemID] then
        BastionLootDB._CACHE_ITEM_DATA[itemID] = {-1,-1,itemEquipLoc}
      elseif BastionLootDB._CACHE_ITEM_DATA[itemID][1]==-1 then
        local itemAsync = Item:CreateFromItemID(itemID)
        itemAsync:ContinueOnItemLoad(function()
          count = count + 1
          local quality = itemAsync:GetItemQuality()
          local iLevel = itemAsync:GetCurrentItemLevel()
          if quality == 4 and iLevel >= 359 then
            local iLink = itemAsync:GetItemLink()
            stats.ITEM_MOD_RESILIENCE_RATING = nil
            bepgp.GetItemStats(iLink,stats)
            if not stats.ITEM_MOD_RESILIENCE_RATING then
              local iID = itemAsync:GetItemID()
              local iName = itemAsync:GetItemName()
              BastionLootDB._CACHE_ITEM_DATA[iID][1] = quality
              BastionLootDB._CACHE_ITEM_DATA[iID][2] = iLevel
              BastionLootDB._CACHE_ITEM_DATA[iID][4] = iName
            end
          end
        end)
      end
    end
  end
  if cacheTimer then
    cacheTimer:Cancel()
  end
  cacheTimer = C_Timer.NewTicker(1,function(self)
    if self._lastCount == count and count > 0 then
      bepgp:debugPrint(string.format("Recalc Caching Loop Finished: %d items",count))
      self:Cancel()
    end
    self._lastCount = count
  end)
  cacheTimer._lastCount = count
  bepgp:debugPrint("Recalc Static Loop Finished")
end

function bepgp_prices_cata:Clean(wipe)
  if not BastionLootDB._CACHE_ITEM_DATA then return end
  if wipe then
    BastionLootDB._CACHE_ITEM_DATA = nil
    return
  end
  for itemID, data in pairs(BastionLootDB._CACHE_ITEM_DATA) do
    if data[1]==-1 then
      BastionLootDB._CACHE_ITEM_DATA[itemID] = nil
    end
  end
end

function bepgp_prices_cata:Distribution(ilvlMin,ilvlMax)
  local count = 0
  local atLevel = {}
  for k,v in pairs(prices) do
    atLevel[v[2]] = atLevel[v[2]] and atLevel[v[2]]+1 or 1
    if ilvlMax then
      if v[2]>=ilvlMin and v[2]<=ilvlMax then
        count = count + 1
      end
    else
      if ilvlMin then
        if v[2]==ilvlMin then
          count = count + 1
        end
      end
    end
  end
  if ilvlMax then
    bepgp:debugPrint(format("Items between %d-%d: %d",ilvlMin,ilvlMax,count))
  elseif ilvlMin then
    bepgp:debugPrint(format("Items at %d iLvl: %d",ilvlMin, count))
  end
  local keys = GetKeysArray(atLevel)
  table.sort(keys)
  for _,iLvl in ipairs(keys) do
    bepgp:debugPrint(format("Items at %d: %d",iLvl,atLevel[iLvl]))
  end
end

